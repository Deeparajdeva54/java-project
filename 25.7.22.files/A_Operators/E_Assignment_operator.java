package myFolder;

public class E_Assignment_operator {

	public static void main(String[] args) {
	
		int a = 10;
		int b = 5;
		
		a += b; // a = a + b;		
		System.out.println(a);
		
		a -= b; // a = a - b;
		System.out.println(a);
		
		a *= b; // a = a * b;
		System.out.println(a);
		
		a /= b; // a = a / b;
		System.out.println(a);
		
		a^= b; // a = a^b;
		
		a = 5  ^  3;
		System.out.println(a);
		
		 double x = Math.pow(5, 3);
		 System.out.println(x);
	}
	

}
